#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(int))
{
	t_list	*tmp;
	t_list	*cr;

	if (!lst || !del)
		return ;
	cr = *lst;
	while (cr != NULL)
	{
		tmp = cr->next;
		del(cr->cont);
		free(cr);
		cr = tmp;
	}
	*lst = NULL;
}
