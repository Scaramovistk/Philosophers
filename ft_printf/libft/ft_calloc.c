#include "libft.h"

void	*ft_calloc(size_t n, size_t size)
{
	size_t	i;
	char	*p;

	i = 0;
	p = malloc(n * size);
	if (p == NULL)
		return (NULL);
	while (i < size * n)
	{
		p[i] = 0;
		i++;
	}
	return (p);
}
